#include <stdio.h>
#include <string.h>
#define _CRT_SECURE_NO_WARNINGS

//이분 탐색 알고리즘을 활용하는 문제 중 하나로

int main(void)
{
	int N, M,num1,num2;
	scanf("%d%d", &N);
	//이 수들은 배열 안에 숫자를 넣어야 되어야 한다는 점.
	for (int i = 0; i < N;i++)
	{
		scanf("%d", &num1);
	}
	scanf("%d%d", &M);
	for (int i = 0; i < M; i++)
	{
		scanf("%d", &num2);
	}
}